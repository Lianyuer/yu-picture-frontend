/** 创建用户接口 POST /api/user/add */
export async function addUserUsingPost(body: API.UserAddDTO, options?: { [key: string]: any }) {
  return request<API.BaseResponseLong_>('/api/user/add', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    data: body,
    ...(options || {}),
  })
}

